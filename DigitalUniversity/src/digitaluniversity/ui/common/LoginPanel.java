    private void BtnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLoginActionPerformed
        Object bObj = this.getClientProperty("business");
        Object rObj = this.getClientProperty("root");

        // Validate client property setup
        if (!(bObj instanceof Business) || !(rObj instanceof JPanel)) {
            lblMessage.setText("Internal error");
            return;
        }

        Business business = (Business) bObj;
        JPanel root = (JPanel) rObj;

        String u = fieldUsername.getText().trim();
        String p = new String(fieldPassword.getPassword());

        // Authenticate user
        UserAccount ua = business.getAccounts().authenticate(u, p);
        if (ua == null) {
            lblMessage.setText("Invalid credentials or inactive user");
            return;
        }

        // Load access control layer based on role
        AccessControlLayer acl = new AccessControlLayer(root, business, ua);
        root.add("acl", acl);

        // Navigate to the next view
        CardLayout cl = (CardLayout) root.getLayout();
        cl.show(root, "acl");
    }//GEN-LAST:event_BtnLoginActionPerformed

