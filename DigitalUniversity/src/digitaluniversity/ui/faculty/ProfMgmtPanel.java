    /**
     * Enables or disables edit fields based on mode.
     *
     * @param editable true to make certain fields editable
     */
    private void configureFields(boolean editable) {
        fieldName.setEditable(false);
        fieldEmail.setEditable(false);
        fieldContact.setEditable(editable);
        fieldDepartment.setEditable(false);
        fieldOfficeHours.setEditable(editable);
        fieldOfficeCollection.setEditable(editable);
    }

    /**
     * Loads current faculty details into form fields.
     */
    private void loadProfile() {
        Business b = getBusiness();
        UserAccount acc = getAccount();

        if (b == null || acc == null) {
            JOptionPane.showMessageDialog(this, "Business context not found.");
            return;
        }

        currentFaculty = b.getFacultyById(acc.getPersonId());
        if (currentFaculty == null) {
            JOptionPane.showMessageDialog(this, "Faculty record not found.");
            return;
        }

        // Set field values from faculty object
        fieldName.setText(currentFaculty.getName());
        fieldEmail.setText(currentFaculty.getEmail());
        fieldContact.setText(currentFaculty.getContact() != null ? currentFaculty.getContact() : "");
        fieldDepartment.setText(currentFaculty.getDepartment());
        fieldOfficeHours.setText(currentFaculty.getOfficeHours() != null ? currentFaculty.getOfficeHours() : "");
        fieldOfficeCollection.setText(currentFaculty.getOfficeLocation() != null ? currentFaculty.getOfficeLocation() : "");

        configureFields(false);
    }

