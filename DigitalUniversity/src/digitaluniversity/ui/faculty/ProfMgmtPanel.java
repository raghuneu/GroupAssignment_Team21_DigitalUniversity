    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (currentFaculty == null) {
            JOptionPane.showMessageDialog(this, "No faculty selected.");
            return;
        }

        currentFaculty.setContact(fieldContact.getText().trim());
        currentFaculty.setOfficeHours(fieldOfficeHours.getText().trim());
        currentFaculty.setOfficeLocation(fieldOfficeCollection.getText().trim());

        JOptionPane.showMessageDialog(this, "Profile updated successfully!");

        configureFields(false);
        btnEdit.setEnabled(true);
        btnSave.setEnabled(false);
        btnCancel.setEnabled(false);
    }//GEN-LAST:event_btnSaveActionPerformed


