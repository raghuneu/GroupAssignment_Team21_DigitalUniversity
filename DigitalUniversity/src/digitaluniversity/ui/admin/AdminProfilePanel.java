    // Loads admin details from the Business model and populates the fields on screen.
    private void loadProfile() {
        Business b = getBusiness();
        UserAccount ua = getAccount();
        if (b == null || ua == null) {
            return;
        }

        if (ua.getRole() != Role.ADMIN) {
            javax.swing.JOptionPane.showMessageDialog(this, "Not an admin account");
            return;
        }

        Admin admin = b.getAdminById(ua.getPersonId());
        if (admin == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Admin profile not found");
            return;
        }

        fieldName.setText(admin.getName());
        fieldEmail.setText(admin.getEmail());
        fieldUsername.setText(ua.getUsername());
        fieldRole.setText(ua.getRole().name());
        jPasswordField1.setText(""); // leave blank to keep current password
        setEditable(false);
    }

