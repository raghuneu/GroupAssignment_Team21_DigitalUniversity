    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // "Save" button validates input, updates Admin data model, and saves changes.
        Business b = getBusiness();
        UserAccount ua = getAccount();
        if (b == null || ua == null) {
            return;
        }

        Admin admin = b.getAdminById(ua.getPersonId());
        if (admin == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Admin profile not found");
            return;
        }

        String newName = fieldName.getText().trim();
        String newEmail = fieldEmail.getText().trim();
        String newPass = new String(jPasswordField1.getPassword()).trim();

        if (newName.isEmpty() || newEmail.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Name and Email are required");
            return;
        }

        admin.setName(newName);
        admin.setEmail(newEmail);
        if (!newPass.isBlank()) {
            ua.setPassword(newPass);
        }

        javax.swing.JOptionPane.showMessageDialog(this, "Profile updated successfully");
        jPasswordField1.setText("");
        setEditable(false);
        loadProfile();
    }//GEN-LAST:event_btnSaveActionPerformed

